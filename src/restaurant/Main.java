    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        connection();
        int i = Integer.parseInt(txtSum.getText());
        int ii = Integer.parseInt(txtPrice.getText());
        int t = i - ii;
        String f = String.valueOf(t);
        txtSum.setText(f);
        
        
        try
        {
            String id = txtID.getText();
            if (id.equals("0"))
            {
                JOptionPane.showMessageDialog(null, "No Data Selected");
            }
            else
            {
                PreparedStatement ps = cn.prepareStatement("delete from tblcart where id like '"+id+"'");
                ps.execute();
                cn.close();
                
                loadrecord1(jTable3);
                txtID.setText("0");
                txtFood.setText("");
                txtPrice.setText("");
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        connection();
        String id = txtID.getText();
        String fud = txtFood.getText();
        String price = txtPrice.getText();

        int prices = Integer.parseInt(price);
        int qty = Integer.parseInt(txtqty.getText());
        int total = prices * qty;
        String tot = String.valueOf(total);
        String Quant = String.valueOf(qty);

        try
        {
            PreparedStatement ps = cn.prepareStatement("insert into tblcart (id, Foods, Quantity, Price)values (?,?,?,?)");
            ps.setString(1, id);
            ps.setString(2, fud);
            ps.setString(3, Quant);
            ps.setString(4, tot);

            ps.execute();
            loadrecord(jTable1);
            cn.close();

            txtID.setText("0");
            txtFood.setText("");
            txtPrice.setText("");
            loadrecord1(jTable3);
            int sum =+ total;
            txtSum.setText(""+sum);

        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

