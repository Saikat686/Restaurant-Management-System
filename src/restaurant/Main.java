    public void loadrecord(JTable table)
    {
        connection();
        try
        {
            PreparedStatement ps = cn.prepareStatement("Select *from tblfoods");
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel)table.getModel();
            model.setRowCount(0);
            Object [] row;
            while (rs.next())
            {
                row = new Object[3];
                row [0] = rs.getInt(1);
                row [1] = rs.getString(2);
                row [2] = rs.getInt(3);
                model.addRow(row);
            }
            cn.close();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
    public void loadrecord1(JTable table)
    {
        connection();
        try
        {
            PreparedStatement ps = cn.prepareStatement("Select *from tblcart");
            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel)table.getModel();
            model.setRowCount(0);
            Object [] row;
            while (rs.next())
            {
                row = new Object[4];
                row [0] = rs.getInt(1);
                row [1] = rs.getString(2);
                row [2] = rs.getString(3);
                row [3] = rs.getInt(4);
                model.addRow(row);
            }
            cn.close();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
